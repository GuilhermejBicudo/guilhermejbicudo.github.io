const all = (selector) => [
  ...document.querySelectorAll(selector)
];


const html = (element, value) => {

  if (element) {
    element.innerHTML = value;
  }

};



function setLanguage(language) {

  const text = i18n[language];

  if (!text) return;


  document.documentElement.lang = text.lang;



  // =========================
  // MENU
  // =========================

  all(".nav-links > a")
    .forEach((item, index) => {

      html(
        item,
        text.menu[index]
      );

    });



  // =========================
  // HERO
  // =========================

  html(
    document.querySelector(".hero .eyebrow"),
    text.hero[0]
  );


  html(
    document.querySelector(".hero h1"),
    text.hero[1]
  );


  html(
    document.querySelector(".hero-description"),
    text.hero[2]
  );


  all(".hero-actions a")
    .forEach((item, index) => {

      html(
        item,
        text.hero[index + 3]
      );

    });



  // =========================
  // ABOUT
  // =========================

  html(
    document.querySelector(".about h2"),
    text.about[0]
  );


  all(".about-text p")
    .forEach((item, index) => {

      html(
        item,
        text.about[index + 1]
      );

    });


  html(
    document.querySelector(".about-text .text-link"),
    text.about[4]
  );



  // =========================
  // SECTION LABELS
  // =========================

  all(".section-label")
    .forEach((item, index) => {

      html(
        item,
        text.labels[index]
      );

    });



  // =========================
  // EXPERIENCE HEADER
  // =========================

  html(
    document.querySelector(".experience-heading h2"),
    text.experience[0]
  );


  html(
    document.querySelector(".experience-heading p"),
    text.experience[1]
  );



  // =========================
  // EXPERIENCE CARDS
  // =========================

  all(".role")
    .forEach((role, index) => {

      const data = text.roles[index];

      if (!data) return;


      html(
        role.querySelector(".role-company"),
        data.company
      );


      html(
        role.querySelector("h3"),
        data.title
      );


      role.querySelectorAll(".role-details li")
        .forEach((item, bulletIndex) => {

          html(
            item,
            data.bullets[bulletIndex]
          );

        });

    });



  // =========================
  // EDUCATION
  // =========================

  all(".education-item")
    .forEach((item, index) => {

      const data = text.education[index];

      if (!data) return;


      html(
        item.querySelector("h3"),
        data.title
      );


      html(
        item.querySelector("p"),
        data.institution
      );

    });



  // =========================
  // PROJECTS
  // =========================

  html(
    document.querySelector(".project-intro h2"),
    text.project[0]
  );


  all(".project-card h3")
    .forEach((item, index) => {

      html(
        item,
        text.project[index * 2 + 1]
      );

    });


  all(".project-card p")
    .forEach((item, index) => {

      html(
        item,
        text.project[index * 2 + 2]
      );

    });


  all(".project-link")
    .forEach((item) => {

      html(
        item,
        text.project[5]
      );

    });



  // =========================
  // CONTACT
  // =========================

  html(
    document.querySelector(".contact .eyebrow"),
    text.contact[0]
  );


  html(
    document.querySelector(".contact h2"),
    text.contact[1]
  );


  html(
    document.querySelector(".contact h2 + p"),
    text.contact[2]
  );


  html(
    document.querySelector(".footer > a:last-child"),
    text.contact[3]
  );



  // =========================
  // LANGUAGE BUTTON STATUS
  // =========================

  all("[data-language]")
    .forEach((item) => {

      item.toggleAttribute(
        "aria-current",
        item.dataset.language === language
      );

    });


  localStorage.setItem(
    "portfolio-language",
    language
  );


}

// =========================
// LANGUAGE SWITCH
// =========================

all("[data-language]")
.forEach((item) => {

  item.addEventListener("click", (event) => {

    event.preventDefault();


    setLanguage(
      item.dataset.language
    );

  });

});



// =========================
// MOBILE MENU
// =========================

const menuButton =
  document.querySelector(".menu-button");


const navLinks =
  document.querySelector(".nav-links");



menuButton?.addEventListener(
  "click",
  () => {

    const open =
      navLinks.classList.toggle("open");


    menuButton.setAttribute(
      "aria-expanded",
      String(open)
    );

  }
);



// Fecha menu após clicar em um link

all(".nav-links a")
.forEach((link) => {

  link.addEventListener(
    "click",
    () => {

      navLinks.classList.remove(
        "open"
      );


      menuButton?.setAttribute(
        "aria-expanded",
        "false"
      );

    }
  );

});



// =========================
// SCROLL REVEAL ANIMATION
// =========================

const observer =
new IntersectionObserver(

  (entries) => {

    entries.forEach(
      (entry) => {

        if (entry.isIntersecting) {

          entry.target.classList.add(
            "visible"
          );


          observer.unobserve(
            entry.target
          );

        }

      }
    );

  },

  {
    threshold: 0.12
  }

);



all(".reveal")
.forEach(
  (element) => {

    observer.observe(
      element
    );

  }
);



// =========================
// YEAR
// =========================

const year =
document.querySelector("#year");


if (year) {

  year.textContent =
    new Date().getFullYear();

}



// =========================
// INITIAL LANGUAGE LOAD
// =========================


const savedLanguage =
localStorage.getItem(
  "portfolio-language"
);



setLanguage(

  savedLanguage ||

  (
    document.documentElement.lang.startsWith("pt")
      ? "pt"
      : "en"
  )

);